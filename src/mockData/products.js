const products = [];

Object.entries(productNames).forEach(([category, items]) => {
  const catBrands = brands[category] || ['Generic'];
  items.forEach((item, idx) => {
    const [name, description, subCategory, price, origPrice, discount, rating, inStock] = item;
    const brand = catBrands[idx % catBrands.length];
    const id = `prod_${String(prodId).padStart(3, '0')}`;
    products.push(product({
      id,
      name,
      slug: name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '') + '-' + prodId,
      description: description + '. High quality product from ' + brand + '.',
      category,
      subCategory,
      brand,
      price,
      originalPrice: origPrice,
      discount,
      rating: rating + (Math.random() * 0.3),
      reviewCount: Math.floor(Math.random() * 500) + 50,
      images: [img(id + 'a'), img(id + 'b'), img(id + 'c')],
      inStock,
      stockCount: inStock ? Math.floor(Math.random() * 50) + 5 : 0,
      specifications: {
        'Brand': brand,
        'Category': category,
        'Warranty': '1 Year',
      },
      features: ['Quality assured', 'Fast delivery', 'Easy returns'],
      tags: [category.toLowerCase(), subCategory.toLowerCase(), brand.toLowerCase()],
      featured: idx < 3,
      isNew: idx % 5 === 0,
    }));
    prodId++;
  });
});

